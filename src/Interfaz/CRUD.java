    private void bnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnIngresarActionPerformed
        String nombre, clave, direccion, telefono;
        
        nombre = txtNombre.getText();
        clave = txtClave.getText();
        direccion = txtDireccion.getText();
        telefono = txtTelefono.getText();
        
        Empleado emp = new Empleado(clave, nombre, direccion, telefono);
        
        DAOEmpleado daoemp = new DAOEmpleado();
        
        daoemp.IngresarEmpleado(emp.getClave(), emp.getNombre(), emp.getDireccion(), emp.getTelefono());
        
        
        
    }//GEN-LAST:event_bnIngresarActionPerformed

