    private void bnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnActualizarActionPerformed
        // TODO add your handling code here:
       
        String nombre, clave, direccion, telefono, clv2 ;
        nombre = txtNombre.getText();
        clave = txtClave.getText();
        direccion = txtDireccion.getText();
        telefono = txtTelefono.getText();
        clv2 = txtClave2.getText();
        
        Empleado temp = new Empleado(clave, nombre, direccion, telefono);
        DAOEmpleado daoTemp = new DAOEmpleado();
        
        daoTemp.ActualizarEmpleado(temp.getClave(), temp.getNombre(), temp.getDireccion(), temp.getTelefono(), clv2);
        
        
    }//GEN-LAST:event_bnActualizarActionPerformed

